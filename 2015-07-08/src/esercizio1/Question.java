public class Question {

    private String value;

    private Answer answer;

    public Question(String value) {
        this.value = value;
    }

    public Answer getAnswer() {
        return answer;
    }

    public void setAnswer(Answer answer) {
        this.answer = answer;
    }

    public String getBestAnswer() {
        return answer.getValue();
    }
}
